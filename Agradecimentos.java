package beerback.trabahodegraduacao.com.beerbackfinal;

import android.content.Intent;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.Toast;

public class Agradecimentos extends AppCompatActivity {

    private Button imprimirid,sair;
    private Button voltarascompras;


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_agradecimentos);
        inicializa();
        eventoclick();
    }


    private void inicializa(){
        imprimirid=(Button)findViewById(R.id.imprimirid);
        voltarascompras=(Button)findViewById(R.id.voltarascomprasid);
        sair = (Button)findViewById(R.id.sairid);

    }



    private void eventoclick()
    {
        imprimirid.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Toast.makeText(Agradecimentos.this, "Nenhum dispositivo impressora conectado", Toast.LENGTH_SHORT).show();
            }
        });

        voltarascompras.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Intent intent = new Intent(Agradecimentos.this, Compras.class);
                startActivity(intent);
                finish();
            }
        });


        sair.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {


            }
        });


    }

}































































































































































































































































































































































































































































































































